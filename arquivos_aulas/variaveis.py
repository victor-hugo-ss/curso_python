# Variáveis são usadas para armazenar valores na memória
# PEP8: inicia com letra minúscula, pode conter números e _
# O sinal de igual (=) é o operador de atribuição.
# Ele atribui o valor à direita para a variável à esquerda.
# Uso: nome_da_variavel = expressão ou valor

nome = 'Victor Hugo'
sobrenome = 'Santos'
idade = 24
ano_nascimento = 2001
maior_de_idade = idade >= 18
altura_metros = 1.70

print('Nome:', nome)
print('Sobrenome:', sobrenome)
print('Idade:', idade)
print('Ano de nascimento:', ano_nascimento)
print('É maior de idade?', maior_de_idade)
print('Altura em metros:', altura_metros)

