# Operações condicionais em Python
# Este código demonstra o uso de estruturas condicionais (if, elif, else) em Python.

# if significa "se", elif significa "senão se" e else significa "senão".

# Exemplo de uso:
entrada = input('Você que "Entrar" ou "Sair"? ')

if entrada == 'entrar':
    print('Você entrou no sistema')

elif entrada == 'sair': 
    print('Você saiu do sistema')

else: 
    print('você não digitou nem entrar e nem sair')